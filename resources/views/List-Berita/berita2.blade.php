            <p>
                <strong>YOGYAKARTA, ATMA.TV</strong> – Dalam rangka memperingati Bulan Peduli Kanker yang jatuh setiap bulan Oktober, Atma Hospital menyelenggarakan seminar kesehatan dengan tema <em>“Deteksi Dini Kanker Payudara dan Pencegahannya”</em> pada tanggal 3 Oktober 2022. Seminar ini bertujuan untuk meningkatkan kesadaran masyarakat, khususnya wanita, tentang pentingnya melakukan deteksi dini kanker payudara serta memahami langkah-langkah pencegahan yang dapat diambil.
            </p>

            <p>
                Acara ini dihadiri oleh lebih dari 200 peserta, mulai dari masyarakat umum, pasien, hingga tenaga medis. Seminar ini juga menghadirkan beberapa narasumber ahli, termasuk dokter spesialis onkologi dan ahli bedah payudara dari Atma Hospital, yang memberikan edukasi mengenai pentingnya pemeriksaan payudara secara rutin, seperti <strong>breast self-examination</strong> (SADARI), serta pemeriksaan medis seperti mammografi dan USG payudara.
            </p>

            <p>
                Dalam sambutannya, dr. Sinta Wardani, Sp.Onk, salah satu pembicara utama, menekankan bahwa deteksi dini adalah kunci untuk meningkatkan angka kesembuhan pada pasien kanker payudara. “Ketika kanker payudara dideteksi pada tahap awal, kemungkinan penyembuhannya jauh lebih tinggi, bahkan bisa mencapai 98%. Oleh karena itu, sangat penting bagi wanita untuk tidak menunda pemeriksaan payudara rutin,” ujar dr. Sinta.
            </p>

            <p>
                Selain pemaparan dari para ahli, acara ini juga dilengkapi dengan sesi tanya jawab interaktif di mana peserta bisa langsung bertanya kepada para dokter mengenai kekhawatiran dan informasi lebih lanjut tentang kanker payudara. Atma Hospital juga menyediakan fasilitas pemeriksaan mammografi gratis bagi 50 peserta pertama sebagai bentuk dukungan langsung dalam pencegahan kanker payudara.
            </p>

            <p>
                Atma Hospital berharap kegiatan ini dapat meningkatkan kesadaran masyarakat akan pentingnya melakukan deteksi dini serta mendorong para wanita untuk lebih memperhatikan kesehatan payudara mereka. "Kami berkomitmen untuk terus mengadakan kegiatan edukatif seperti ini, guna membantu masyarakat memahami pentingnya deteksi dini dan langkah-langkah pencegahan penyakit, termasuk kanker payudara," ungkap dr. Indri Kartika, Direktur Pelayanan Medis Atma Hospital.
            </p>

            <p>
                Acara seminar ini merupakan bagian dari rangkaian kegiatan Bulan Peduli Kanker yang diadakan Atma Hospital setiap tahunnya. Selain seminar, rumah sakit juga berencana untuk mengadakan program screening kanker gratis bagi masyarakat yang membutuhkan dalam waktu dekat.
            </p>

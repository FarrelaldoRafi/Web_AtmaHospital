                <strong>YOGYAKARTA, ATMA.TV</strong> Atma Hospital Mengadakan Sesi Imunisasi Anak Gratis untuk Meningkatkan <strong>Kesehatan Masyarakat</strong> pada Minggu (17/09/2022).
            </p>

            <p>
                Dalam rangka meningkatkan kesehatan masyarakat, khususnya anak-anak, Atma Hospital kembali menunjukkan komitmennya terhadap kesehatan publik dengan mengadakan sesi imunisasi anak gratis. Program yang berlangsung pada minggu lalu ini mendapat sambutan hangat dari masyarakat setempat. Ratusan orang tua terlihat antusias membawa anak-anak mereka untuk mendapatkan imunisasi gratis, memastikan anak-anak terlindungi dari berbagai penyakit yang dapat dicegah.
            </p>

            <p>
                Acara imunisasi ini digelar sebagai bagian dari perayaan ulang tahun Atma Hospital yang ke-56, dan menjadi salah satu bentuk kontribusi nyata rumah sakit terhadap masyarakat. Selain imunisasi, para orang tua juga mendapatkan edukasi tentang pentingnya menjaga kesehatan anak melalui imunisasi rutin.
            </p>

            <p>
                Dokter spesialis anak di Atma Hospital, dr. Narji Sandoro, Sp.A, menyampaikan bahwa imunisasi adalah salah satu cara paling efektif untuk melindungi anak-anak dari penyakit serius seperti campak, polio, dan difteri. â€œMelalui imunisasi, kita dapat memberikan perlindungan jangka panjang bagi anak-anak, yang merupakan generasi penerus bangsa. Kami berharap dengan adanya kegiatan ini, semakin banyak anak yang terlindungi dari berbagai penyakit yang dapat dicegah,â€ ujar dr. Narji.
            </p>

            <p>
                Tidak hanya itu, sesi ini juga dilengkapi dengan konsultasi gratis yang diberikan oleh tim dokter spesialis anak, sehingga para orang tua dapat langsung berkonsultasi terkait kesehatan anak mereka. Selain imunisasi, Atma Hospital juga memberikan informasi tentang pentingnya pola makan sehat dan pemantauan tumbuh kembang anak secara berkala.
            </p>

            <p>
                Atma Hospital berkomitmen untuk terus mengadakan kegiatan serupa di masa mendatang sebagai bagian dari upaya meningkatkan kesadaran kesehatan di masyarakat. Dengan adanya program-program seperti ini, diharapkan angka penyakit yang dapat dicegah melalui imunisasi akan terus menurun, dan kesehatan anak-anak di Indonesia semakin terjaga.

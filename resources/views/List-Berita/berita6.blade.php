                <strong>YOGYAKARTA, ATMA.TV</strong> â€“ Sebagai salah satu langkah inovatif dalam memberikan akses kesehatan yang lebih mudah dan cepat bagi masyarakat, Atma Hospital secara resmi meluncurkan layanan <strong>telemedicine</strong> pada tanggal 21 November 2023. Dengan layanan ini, masyarakat dapat berkonsultasi dengan dokter secara online tanpa harus datang ke rumah sakit, sehingga mempermudah akses terhadap layanan kesehatan, terutama bagi mereka yang tinggal di daerah terpencil atau memiliki keterbatasan waktu.
            </p>

            <p>
                Melalui telemedicine, pasien dapat melakukan konsultasi dengan dokter umum maupun spesialis, menerima resep obat, hingga mendapatkan rujukan untuk pemeriksaan lanjutan. Semua ini dapat dilakukan hanya melalui smartphone atau perangkat elektronik lainnya yang terhubung ke internet. "Peluncuran layanan telemedicine ini adalah bagian dari komitmen kami untuk memberikan pelayanan kesehatan yang cepat dan efisien, terutama di era digital seperti sekarang," ujar Direktur Pelayanan Medis Atma Hospital, dr. Indri Kartika.
            </p>

            <p>
                Telemedicine Atma Hospital juga dirancang dengan standar keamanan data yang tinggi, sehingga privasi pasien tetap terjaga. Para dokter yang bertugas di layanan ini juga telah mendapatkan pelatihan khusus untuk memberikan konsultasi medis secara online, memastikan bahwa kualitas pelayanan tetap prima meskipun dilakukan dari jarak jauh.
            </p>

            <p>
                "Kami memahami bahwa tidak semua masyarakat bisa datang langsung ke rumah sakit, terutama saat kondisi mendesak atau di tengah pandemi. Dengan layanan telemedicine, kami berharap masyarakat tetap bisa mendapatkan pelayanan kesehatan yang optimal tanpa mengorbankan kenyamanan mereka," tambah dr. Indri. 
            </p>

            <p>
                Selain konsultasi kesehatan, layanan ini juga memungkinkan pasien untuk memesan obat secara online dan langsung dikirimkan ke rumah mereka. Atma Hospital juga akan bekerja sama dengan berbagai apotek dan penyedia layanan pengiriman untuk memastikan obat yang dibutuhkan pasien tiba tepat waktu.
            </p>

            <p>
                Layanan telemedicine ini dapat diakses melalui aplikasi khusus yang sudah tersedia di platform Android dan iOS. Pasien cukup mendaftarkan diri, memilih dokter, dan memulai sesi konsultasi langsung dari rumah. Atma Hospital berharap layanan ini dapat membantu meningkatkan akses kesehatan yang lebih merata di seluruh wilayah, khususnya di daerah-daerah yang jauh dari fasilitas kesehatan.

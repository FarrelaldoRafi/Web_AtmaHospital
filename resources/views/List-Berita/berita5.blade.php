                <strong>YOGYAKARTA, ATMA.TV</strong> â€“ Dalam rangka memperingati Hari Kesehatan Nasional yang jatuh pada tanggal 12 November setiap tahun, Atma Hospital mengadakan serangkaian kegiatan sosial dan edukatif untuk meningkatkan kesadaran masyarakat akan pentingnya menjaga kesehatan. Acara yang berlangsung pada tanggal 12 November 2023 ini bertujuan untuk memberikan edukasi mengenai kesehatan serta menyediakan layanan pemeriksaan gratis kepada masyarakat Yogyakarta dan sekitarnya.
            </p>

            <p>
                Acara ini dimulai dengan kegiatan senam bersama di halaman depan rumah sakit yang diikuti oleh lebih dari 200 peserta. Selain itu, Atma Hospital juga menyediakan stand pemeriksaan kesehatan gratis yang meliputi pemeriksaan tekanan darah, gula darah, kolesterol, dan konsultasi medis langsung dengan dokter spesialis. "Kami ingin merayakan Hari Kesehatan Nasional ini dengan memberikan manfaat nyata kepada masyarakat. Kesehatan adalah aset utama setiap individu, dan kami ingin menjadi bagian dari perjalanan menjaga kesehatan tersebut," ujar dr. Indri Kartika, Direktur Pelayanan Medis Atma Hospital.
            </p>

            <p>
                Sebagai bagian dari perayaan ini, Atma Hospital juga mengadakan seminar kesehatan dengan tema <strong>"Pencegahan Penyakit Jantung dan Diabetes"</strong>, yang disampaikan oleh dokter spesialis jantung dan spesialis penyakit dalam. Seminar ini menarik perhatian banyak peserta karena memberikan pengetahuan penting mengenai cara menjaga pola makan dan gaya hidup yang sehat untuk mencegah penyakit kronis. 
            </p>

            <p>
                "Dengan meningkatnya angka penderita penyakit jantung dan diabetes di Indonesia, sangat penting bagi masyarakat untuk memahami cara-cara pencegahan yang efektif. Kami berharap melalui seminar ini, masyarakat akan lebih sadar dan termotivasi untuk menjalani gaya hidup sehat," tambah dr. Indri.
            </p>

            <p>
                Selain itu, Atma Hospital juga membagikan paket sembako kepada masyarakat kurang mampu yang hadir dalam acara tersebut sebagai bentuk kepedulian terhadap kesejahteraan mereka. Acara ini diakhiri dengan sesi donor darah yang diikuti oleh puluhan peserta, bekerja sama dengan Palang Merah Indonesia (PMI) untuk membantu meningkatkan stok darah di rumah sakit dan PMI Yogyakarta.
            </p>

            <p>
                Atma Hospital berkomitmen untuk terus mengadakan kegiatan serupa di masa mendatang, sebagai bagian dari tanggung jawab sosial rumah sakit untuk membantu meningkatkan kesehatan masyarakat secara keseluruhan. "Kami berharap kegiatan ini dapat memberikan dampak positif yang berkelanjutan bagi masyarakat, tidak hanya di Hari Kesehatan Nasional tetapi setiap hari," tutup dr. Indri.
            </p>

            <p>
                <strong>YOGYAKARTA, ATMA.TV</strong> – Dalam rangka mendukung ketersediaan stok darah di Palang Merah Indonesia (PMI), Atma Hospital menyelenggarakan acara donor darah massal pada tanggal 1 November 2023. Kegiatan ini tidak hanya bertujuan untuk membantu memenuhi kebutuhan darah di rumah sakit, tetapi juga untuk meningkatkan kesadaran masyarakat akan pentingnya mendonorkan darah secara rutin.
            </p>

            <p>
                Acara donor darah yang berlangsung di lobi utama Atma Hospital ini berhasil menarik lebih dari 250 peserta dari berbagai kalangan, termasuk tenaga medis, karyawan, dan masyarakat umum. Dengan mengikuti protokol kesehatan yang ketat, para peserta dapat mendonorkan darahnya dengan aman. "Setiap tetes darah yang disumbangkan sangat berarti, tidak hanya bagi mereka yang membutuhkannya, tetapi juga bagi kesehatan para pendonor," ujar dr. Indri Kartika, Direktur Pelayanan Medis Atma Hospital.
            </p>

            <p>
                Donor darah memiliki banyak manfaat, baik bagi penerima maupun pendonor. Selain membantu pasien yang membutuhkan transfusi, donor darah juga bermanfaat bagi pendonor karena dapat membantu memperbarui sel darah merah dan meningkatkan kesehatan jantung. Tim medis Atma Hospital juga memberikan edukasi kepada para pendonor mengenai manfaat kesehatan dari mendonorkan darah secara rutin.
            </p>

            <p>
                “Melalui kegiatan ini, kami berharap dapat membantu meningkatkan kesadaran masyarakat akan pentingnya donor darah dan berkontribusi langsung dalam menyelamatkan nyawa,” tambah dr. Indri. Selain donor darah, para peserta juga mendapatkan cek kesehatan gratis yang meliputi pemeriksaan tekanan darah, kadar hemoglobin, serta konsultasi medis singkat untuk memastikan bahwa mereka layak mendonorkan darah.
            </p>

            <p>
                Atma Hospital berkomitmen untuk terus mengadakan kegiatan sosial semacam ini sebagai bentuk kontribusi nyata terhadap kesehatan masyarakat. Dengan adanya kegiatan donor darah massal ini, diharapkan ketersediaan darah di Yogyakarta, khususnya di rumah sakit dan PMI, tetap terjaga dengan baik, terutama untuk kebutuhan darurat dan operasi.
            </p>

            <p>
                Acara ini diakhiri dengan pemberian sertifikat apresiasi dan suvenir kepada para pendonor sebagai bentuk penghargaan atas kontribusi mereka. "Kami berterima kasih kepada semua peserta yang telah mendonorkan darahnya. Setiap kantong darah sangat berharga dan bisa menyelamatkan nyawa orang lain," tutup dr. Indri.
            </p>

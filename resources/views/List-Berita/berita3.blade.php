            </p>

            <p>
                Acara ini berhasil menarik perhatian lebih dari 300 peserta yang datang untuk memeriksakan kesehatannya secara gratis. Pelayanan yang diberikan meliputi pemeriksaan tekanan darah, cek gula darah, kolesterol, konsultasi medis, serta pemberian obat gratis bagi masyarakat yang membutuhkan. Selain itu, tim dokter spesialis dari Atma Hospital juga memberikan penyuluhan kesehatan tentang pola hidup sehat dan pencegahan penyakit kronis seperti diabetes dan hipertensi.
            </p>

            <p>
                Direktur Pelayanan Medis Atma Hospital, dr. Indri Kartika, menyampaikan bahwa acara ini merupakan bagian dari tanggung jawab sosial rumah sakit kepada masyarakat. "Kami ingin berkontribusi langsung kepada masyarakat, khususnya di wilayah Yogyakarta, melalui program bakti sosial ini. Dengan pemeriksaan gratis dan edukasi kesehatan, kami berharap dapat membantu masyarakat dalam menjaga kesehatan mereka dan mencegah penyakit sejak dini," ujar dr. Indri.
            </p>

            <p>
                Selain pemeriksaan kesehatan, Atma Hospital juga menyediakan layanan vaksinasi gratis bagi anak-anak. Program ini bertujuan untuk meningkatkan cakupan imunisasi anak di wilayah Yogyakarta, guna melindungi mereka dari berbagai penyakit menular. "Kami ingin memastikan bahwa anak-anak mendapatkan haknya untuk hidup sehat melalui imunisasi yang lengkap dan tepat waktu," tambah dr. Indri.
            </p>

            <p>
                Acara bakti sosial ini diakhiri dengan pembagian paket sembako kepada masyarakat yang hadir. Atma Hospital berharap kegiatan ini dapat membantu meringankan beban ekonomi masyarakat sekaligus memberikan mereka kesempatan untuk meningkatkan kualitas hidup melalui layanan kesehatan yang diberikan secara cuma-cuma.
            </p>

            <p>
                Atma Hospital berkomitmen untuk terus mengadakan kegiatan sosial semacam ini di berbagai wilayah Indonesia sebagai bentuk tanggung jawab sosial dan kepedulian terhadap kesehatan masyarakat.
